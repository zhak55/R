proccess <- function(cf, delay = 0.1) {
  l <- length(cf)
  pb <- txtProgressBar(min = 0, max = l, style = 3)
  for(i in cf) {
    Sys.sleep(delay);
    setTxtProgressBar(pb, i);
    print(i);
    flush.console(); # drop for real process 
  }
  close(pb)
}
